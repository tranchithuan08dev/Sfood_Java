package dto;

/**
 *
 * @author Admin
 */
public class Week {
    private int planId; 
    private String name;
    private String day;
    private String week;
     private String description;
    private String type;
   

    public Week() {
    }

    public Week(int planId, String name, String day, String week, String description, String type) {
        this.planId = planId;
        this.name = name;
        this.day = day;
        this.week = week;
        this.description = description;
        this.type = type;
    }

    public int getPlanId() {
        return planId;
    }

    public void setPlanId(int planId) {
        this.planId = planId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    
}
