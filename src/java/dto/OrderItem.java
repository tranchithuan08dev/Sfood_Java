package dto;

/**
 *
 * @author Admin
 */
public class OrderItem {
    private int itemId;
    private String itemName;
    private float price;
    private String image;
    private String type;

    public OrderItem() {
    }

    public OrderItem(int itemId, String itemName, float price, String image, String type) {
        this.itemId = itemId;
        this.itemName = itemName;
        this.price = price;
        this.image = image;
        this.type = type;
    }

    public int getItemId() {
        return itemId;
    }

    public void setItemId(int itemId) {
        this.itemId = itemId;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
  
}
